package com.bocom.dto.img;

import java.io.File;



public class ImageDto {
	
	private File readFile;//读取文件对象
	
	private File writeFile;//写入文件对象
	
	private String startName;	// 转换前  图片名称 
	
	private String format;	// 文件格式

	private String endName;	//转换后名称 
	
	private Boolean canFormat;	//是否为标准转换类型 

	public File getReadFile() {
		return readFile;
	}

	public void setReadFile(File readFile) {
		this.readFile = readFile;
	}

	public File getWriteFile() {
		return writeFile;
	}

	public void setWriteFile(File writeFile) {
		this.writeFile = writeFile;
	}

	public String getStartName() {
		return startName;
	}

	public void setStartName(String startName) {
		this.startName = startName;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getEndName() {
		return endName;
	}

	public void setEndName(String endName) {
		this.endName = endName;
	}

	public Boolean getCanFormat() {
		return canFormat;
	}

	public void setCanFormat(Boolean canFormat) {
		this.canFormat = canFormat;
	}
	
}
