package com.bocom.dto.api;

import java.util.List;

public class AppListDto {
	
	private String applyUser;// 主键
	
	private String applyOrg;// 应用ID
	
	private  List<Integer> refStatus;//0：已部署; 1：已运行 2：卸载中; 3：已卸载; 4：切换中; 5：已关闭

	public String getApplyUser() {
		return applyUser;
	}

	public void setApplyUser(String applyUser) {
		this.applyUser = applyUser;
	}

	public String getApplyOrg() {
		return applyOrg;
	}

	public void setApplyOrg(String applyOrg) {
		this.applyOrg = applyOrg;
	}

	public List<Integer> getRefStatus() {
		return refStatus;
	}

	public void setRefStatus(List<Integer> refStatus) {
		this.refStatus = refStatus;
	}
}
