package com.bocom.domain;

import java.util.Date;

public class AppUninstall {
	/**
	 * 主键
	 */
	private String id;
	/**
	 * 外键appInfo表
	 */
	private String appRefId;
	/**
	 * 应用ID
	 */
	private String appId;
	/**
	 * 应用名称
	 */
	private String appName;
	/**
	 * 应用描述
	 */
	private String appDesc;
	/**
	 * 版本
	 */
	private String version;
	/**
	 * 状态  1:处理中   2:处理成功    3:处理失败
	 */
	private Integer status;
	/**
	 * 管理类型 1： 发布 2：卸载
	 */
	private Integer managType;
	/**
	 * 创建人
	 */
	private String createBy;
	/**
	 * 创建人部门
	 */
	private String createByOrg;
	/**
	 * 创建时间
	 */
	private Date createTime;
	/**
	 * 完成时间
	 */
	private Date finishTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAppRefId() {
		return appRefId;
	}

	public void setAppRefId(String appRefId) {
		this.appRefId = appRefId;
	}

	public String getAppId() {
		return appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getAppDesc() {
		return appDesc;
	}

	public void setAppDesc(String appDesc) {
		this.appDesc = appDesc;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getManagType() {
		return managType;
	}

	public void setManagType(Integer managType) {
		this.managType = managType;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateByOrg() {
		return createByOrg;
	}

	public void setCreateByOrg(String createByOrg) {
		this.createByOrg = createByOrg;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getFinishTime() {
		return finishTime;
	}

	public void setFinishTime(Date finishTime) {
		this.finishTime = finishTime;
	}
}
